/// <summary>
/// Summary description for Calculator
/// </summary>
public class Calculator
{
    public double Add(double a, double e)
    {
        return a + e;
    }

    public double Subtract(double a, double e)
    {
        return a - e;
    }

    public double Multiply(double a, double e)
    {
        return a * e;
    }

    public double Divide (double a, double e)
    {
        return a / e;
    }
}